/**
 * Data-Types-Variables
 */
public class VariablesDataTypes {
    public static void main(String[] args) {
        int myNum = 5; // Integer
        float myFloatNum = 5.99f; // Floating Point Number
        double myDoubleNum = 53.99; // Floating Point Number
        char myLetter = '['; // Character
        boolean myBool = true; // Boolean
        String myText = "Hello"; // Strings In Java

        System.out.println("Integer-Variable " + myNum);
        System.out.println("Float-Variable " + myFloatNum);
        System.out.println("Double-Variable " + myDoubleNum);
        System.out.println("Boolean-Variable " + myBool);
        System.out.println("char-Variable " + myLetter);
        System.out.println("string-Variable " + myText);
    }
};